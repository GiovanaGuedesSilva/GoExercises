package mathutil

/*
	Package mathutil: fornece funções matemáticas básicas.

	- As funções devem começar com letra maiúscula para serem exportadas.
	- Este package pode ser importado por outros arquivos Go dentro do mesmo módulo.
*/

// Função pública
func Somar(a, b int) int {
	return a + b
}

// Outra função pública
func Subtrair(a, b int) int {
	return a - b
}
